# Задача: Функция, возвращающая время в удобочитаемом формате.

# Запишите функцию, которая принимает на вход неотрицательное целое число(секунды) и возвращает
# время в удобочитаемом формате(HH: MM: SS)
# НН = часов, дополнено до 2 цифр, диапазон: 00 - 99
# ММ = минут, дополнено до 2 цифр, диапазон: 00 - 59
# SS = секунд, дополнено до 2 цифр, диапазон: 00 - 59
# Максимальное время никогда не превышает 359999(99:59:59)

# Алгоритм решения задачи:

# 1. Вычислить количество часов, минут и секунд из переданного количества секунд.

# 2. Убедиться, что каждое из чисел(часы, минуты, секунды) имеет правильный
# формат, то есть дополнено до 2 цифр.


def format_time(seconds):
    # Убедимся, что входное значение является неотрицательным целым числом
    if not isinstance(seconds, int) or seconds < 0:
        raise ValueError("Входные данные должны быть неотрицательным целым числом.")

    # Максимальное значение времени в секундах не должно превышать 359999
    if seconds > 359999:
        raise ValueError("Ввод превышает максимальный лимит в 359999 секунд.")

    # Вычисляем количество часов, минут и секунд
    # Часы вычисляются целочисленным делением общего количества секунд на 3600.
    hours = seconds // 3600
    # Минуты вычисляются как остаток от деления общего количества секунд на 3600, разделённый на 60.
    minutes = (seconds % 3600) // 60
    # Секунды вычисляются как остаток от деления общего количества секунд на 60.
    seconds = seconds % 60

    # Форматируем строки для каждого компонента времени, дополняя до 2 цифр
    # Используем форматирование строк с f-string, чтобы дополнить числа до двух цифр с ведущими нулями.
    formatted_time = f"{hours:02}:{minutes:02}:{seconds:02}"

    return formatted_time


# Примеры использования функции
print(format_time(359999))  # Вывод: 99:59:59
print(format_time(0))  # Вывод: 00:00:00
print(format_time(3661))  # Вывод: 01:01:01
print(format_time(60))  # Вывод: 00:01:00
print(format_time(86399))  # Вывод: 23:59:59
