# Задача: Новый модный текстовый редактор.

# Ваша команда пишет новый модный текстовый редактор, и вам поручено реализовать нумерацию строк.
# Запишите функцию, которая берет список строк и возвращает каждую строку, дополненную правильным номером.
# Нумерация начинается с 1. Формат есть n: string. Обратите внимание на двоеточие и пространство между ними.
# Примеры: (Вход --> Выход)
# [] --> []
# ["a", "b", "c"] --> ["1: a", "2: b", "3: c"]


def number_lines(lines):
    """
    Функция принимает список строк и возвращает новый список строк,
    каждая из которых пронумерована.
    """
    # Проверка пустого списка
    if not lines:
        return []

    # Инициализация пустого списка для хранения нумерованных строк
    numbered_lines = []

    # Перебор всех строк в списке lines с помощью функции enumerate, которая возвращает пару (индекс, строка)
    for i, line in enumerate(lines):
        # Формирование нумерованной строки, где i + 1 - это номер строки, а line - оригинальная строка
        numbered_line = f"{i + 1}: {line}"
        # Добавление нумерованной строки в список numbered_lines
        numbered_lines.append(numbered_line)

    return numbered_lines


# Примеры использования функции
print(number_lines([]))  # Вывод: []
print(number_lines(["a", "b", "c"]))  # Вывод: ["1: a", "2: b", "3: c"]
print(number_lines(["Hello", "World", "Test"]))  # Вывод: ["1: Hello", "2: World", "3: Test"]
